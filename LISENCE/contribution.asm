; Contribution Guidelines
; Thank you for considering contributing to Aqu Kernel! Here are some guidelines to help you get started.
; How to Contribute
; 1. Fork the repository.
; 2. Create a new branch for your changes (`git checkout -b feature-branch`).
; 3. Make your changes and commit them (`git commit -m "Describe changes"`).
; 4. Push to your branch (`git push origin feature-branch`).
; 5. Submit a pull request.
; Code of Conduct
; Be respectful and considerate of other contributors.
; By contributing to Aqu Kernel, you agree that your contributions will be licensed under the MIT License.